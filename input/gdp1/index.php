<h3>Rapide rappel sur les ensembles de nombres</h3>
</p>
<p>
$\mathbb{N}$ : ensemble des entiers naturels $ n \in \mathbb{N} \implies n = 0, 1, 2, ...$
</p>
<p><b>Pour dénombrer des ensembles!
</b><hr /></p>
<p>
$\mathbb{Z}$ : ensemble des entiers relatifs $ n \in \mathbb{Z} \implies n =  ... -1, 0, 1, 2, ...$
</p>
<p><b>Permet de donner des valeurs négatives. Si on a une droite graduée,on peut maintenant la centrer en 0 et aller dans les deux sens; 
</b><hr /></p>
<p>
$\mathbb{D}$ : ensemble des décimaux $ a \in \mathbb{D} \implies \exists n \in \mathbb{Z} \textbf{ et } b \in \mathbb{Z} : a = b . 10^{n}$
</p>
<p><b>Les nombre qui ont un nombre fini de chiffres après la virgule;
</b><hr /></p>
<p>
$\mathbb{Q}$ : ensemble des rationnels $ n \in \mathbb{Q} \implies \exists p \in \mathbb{Z} \textbf{ et } q \in \mathbb{N}* : n = p/q$
</p>
<p><b>toutes les fractions de nombres entiers, très grossièrement; 
</b><hr /></p>
<p>
$\mathbb{R}$ : ensemble des réels  $ x \in \mathbb{R} \implies n =  $ n'importe quel nombre;
</p>
<p><b>Contient tous les rationnel ($\mathbb{Q}$) et les autres :-) $i.e$ $\sqrt{2}$, $\pi$, $e$, etc.</b> On dit que $\mathbb{R}$  est le complété de $\mathbb{Q}$  !
<hr /></p>
<p>
$\mathbb{C}$ : ensemble des nombres complexe $ z \in \mathbb{C} \implies z =  a+ i.b$ avec $a$ et $b$ deux réels et $i$ tel que $i^2 = -1$.

