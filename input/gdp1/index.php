<h3>Somme des entiers de $1$ à $N$</h3>
</p>
<p>Soit, calculer par exemple : 
$$
S = 1 + 2 + 3 + ... + 99 + 100
$$
La formule découle d'une observation que Gauss aurait fait vers l'age de huit ans à son professeur. 
Ecrivons la somme dans un sens et dans l'autre:
\begin{align}
		S & =1 \,\,\,\, \,\,+ 2 \,\,\,+ 3 \,\,\,\,+ ... + 99 \,+ 100  \\
		S & = 100 + 99 \,+ 97 + ... + 2 \,\,\,\,+ 1 
\end{align}
On s'aperçoit que $100 + 1 = 101$, puis $99+2=101$ etc. Donc $2S = 100 \times 101$ puisqu'il y a $100$ termes qui valent tous $101$. D'où on tire que : 
$$
S = (100 \times 101) / 2 
$$
Dans notre cas, la formule de l'addition des $N$ premiers entiers est la suivante : 
$$
S = \dfrac{N \times (N+1)}{2}
$$
Ce qui permet de calculer de tête la somme des $N$ dans certains cas. 

